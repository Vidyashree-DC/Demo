class Incrementndecrement{
public static void main(String[] args){
int a;
a=10;
System.out.println("Value of A is:" +a);
a++;
System.out.println("Post Increment a value:" +a++);
++a;
System.out.println("Pre Increment a value:" + ++a);
--a;
System.out.println("Pre Decrement a value:" + --a);
a--;
System.out.println("Post Decrement a value:" +a--);
}
}